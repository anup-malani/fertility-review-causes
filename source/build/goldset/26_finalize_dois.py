#!/usr/bin/env python3
"""
Step 26 - Finalize DOIs for the meta-analysis-ready set and emit OUTPUT 1 (markdown DOI list).

DOI resolution follows the C3 production policy with the C2 title guard, layered by trust:
  1. GOLD RA-verified  (tier-a-verified.json canonical_doi)  - highest trust
  2. committed title-verified corrections (wid-doi-corrected-map.json, 275 audited)
  3. fresh guarded OpenAlex DOI (step-23 enrichment, wid_status==OK, title-guarded >=0.5)
  4. UNRESOLVED - flagged with reason (dead/drift W-ID, or no DOI registered)
Where a committed correction and the fresh OpenAlex DOI disagree, the committed (audited)
value wins and the disagreement is flagged for RA spot-check.

Inputs : {slug}-metaanalysis-ready.json, {slug}-wid-doi-corrected-map.json,
         {slug}-tier-a-verified.json
Outputs: {slug}-metaanalysis-ready-final.json  (adds doi_final + doi_trust + flags)
         output/{slug}-metaanalysis-doi-list.md  (OUTPUT 1: the deliverable)
"""
import json, re
from pathlib import Path

ROOT = Path("/Users/shravanhari/~/Anup RA/projects/fertility-review-causes")
SL = ROOT / "literature/search-logs"
OUT = ROOT / "output"; OUT.mkdir(exist_ok=True)
SLUG = "old-age-security-pension-crowdout"

def norm(d):
    return (d or "").replace("https://doi.org/", "").strip().lower() or None

_STOP = {"the","a","an","of","and","in","on","for","from","to","its","by","new","is","with","evidence"}
def _toks(t):
    return {w for w in re.sub(r"[^a-z0-9\s]", " ", (t or "").lower()).split() if w not in _STOP}
def jacc(a, b):
    A, B = _toks(a), _toks(b); return len(A & B) / len(A | B) if (A | B) else 0.0

ET_LABEL = {4: "natural/quasi-experiment", 3: "proxy/IV causal-leaning",
            2: "observational econometrics", 1: "theory", 0: "qualitative"}

def authstr(a):
    if not a: return "n.a."
    return (a[0] if len(a) == 1 else f"{a[0]} et al.") if len(a) > 2 else " & ".join(a)

def main():
    ready = json.load(open(SL / f"{SLUG}-metaanalysis-ready.json"))
    corr = {k: norm(v) for k, v in json.load(open(SL / f"{SLUG}-wid-doi-corrected-map.json")).items()}
    gold = json.load(open(SL / f"{SLUG}-tier-a-verified.json"))
    gold_dois = {norm(g.get("canonical_doi")) for g in gold if g.get("canonical_doi")}
    # gold title -> DOI, for title-based matching (C2): a gold study whose paperId's DOI never
    # resolved (dead W-ID) is still recoverable by matching its title to the RA-verified record.
    gold_td = [(g.get("title") or g.get("registered_title"), norm(g.get("canonical_doi")))
               for g in gold if g.get("canonical_doi")]
    def gold_by_title(title):
        best = max(gold_td, key=lambda p: jacc(title, p[0]) if p[0] else 0, default=(None, None))
        return best[1] if (best[0] and jacc(title, best[0]) >= 0.80) else None
    crp = SL / f"{SLUG}-crossref-recovered.json"
    cross = {k: norm(v["doi"]) for k, v in (json.load(open(crp)) if crp.exists() else {}).items()}

    n_gold = n_corr = n_oa = n_cross = n_unres = n_flag = 0
    for r in ready:
        pid = r["paperId"]; oa = norm(r.get("doi"))
        cm = corr.get(pid)
        final = None; trust = None; flag = None
        if cm:
            final, trust = cm, "corrected-map(title-verified)"; n_corr += 1
            if oa and oa != cm: flag = f"openalex_doi_differs:{oa}"; n_flag += 1
        elif oa and r.get("wid_status") == "OK":
            final, trust = oa, "openalex-fresh(guarded)"; n_oa += 1
        elif cross.get(pid):
            final, trust = cross[pid], "crossref-search(J>=0.80)"; n_cross += 1
        else:
            gt = gold_by_title(r.get("title"))
            if gt:
                final, trust = gt, "gold-RA-verified(title)"; n_gold += 1
            else:
                trust = "UNRESOLVED"; flag = r.get("wid_status"); n_unres += 1
        if final and final in gold_dois and not trust.startswith("gold"):
            trust = "gold-RA-verified"; n_gold += 1
            if cm: n_corr -= 1
            elif r.get("wid_status") == "OK" and oa: n_oa -= 1
            elif cross.get(pid): n_cross -= 1
        r["doi_final"], r["doi_trust"], r["doi_flag"] = final, trust, flag

    json.dump(ready, open(SL / f"{SLUG}-metaanalysis-ready-final.json", "w"), indent=2)

    # OUTPUT 1 - markdown DOI list
    L = []
    L.append("# Old-Age-Security / Pension-Crowdout — Meta-Analysis-Ready DOI List\n")
    L.append(f"**Hypothesis:** old-age-security / pension crowdout of fertility  ")
    mn = min((r["evidenceType"] for r in ready), default=0)
    barlbl = "evidenceType == 4 (natural/quasi-experiments)" if mn >= 4 else f"evidenceType ≥ {mn}"
    L.append(f"**Set:** meta-analysis-ready = (Tier 1 ∪ Tier 2) ∩ empirical evidence bar ({barlbl})  ")
    L.append(f"**n = {len(ready)}**  ·  pipeline: GACS Phase E on the OAS pilot (legacy-migration path)  ")
    L.append(f"**Generated by:** `source/build/goldset/26_finalize_dois.py`\n")
    L.append(f"DOI trust: gold-RA-verified {n_gold} · title-verified {n_corr} · OpenAlex-guarded {n_oa} · Crossref-search {n_cross} · **UNRESOLVED {n_unres}** · openalex/committed disagreements flagged {n_flag}\n")
    L.append("| # | DOI | Title | Authors | Year | Tier | ET | comp | channel | gold | DOI trust |")
    L.append("|--:|---|---|---|--:|:--:|:--:|:--:|:--:|:--:|---|")
    for i, r in enumerate(ready, 1):
        d = r["doi_final"]
        dcell = f"`{d}`" if d else f"⚠ UNRESOLVED ({r['doi_flag']})"
        t = (r["title"] or "")[:80].replace("|", "\\|")
        L.append(f"| {i} | {dcell} | {t} | {authstr(r.get('authors'))} | {r.get('year') or ''} "
                 f"| T{r['tier']} | {r['evidenceType']} | {r['compositeScore']} | {r.get('channel') or ''} "
                 f"| {'★' if r['in_gold'] else ''} | {r['doi_trust']} |")
    L.append("\n## Plain DOI list (resolved only)\n")
    for r in ready:
        if r["doi_final"]: L.append(f"- {r['doi_final']}")
    unres = [r for r in ready if not r["doi_final"]]
    if unres:
        L.append(f"\n## Unresolved ({len(unres)}) — need manual/Crossref resolution\n")
        for r in unres:
            L.append(f"- [{r['doi_flag']}] {r['title']} ({r.get('year') or 'n.d.'}) — {r['paperId']}")
    (OUT / f"{SLUG}-metaanalysis-doi-list.md").write_text("\n".join(L) + "\n")

    print(f"final set: {len(ready)} | gold {n_gold} · corrected {n_corr} · openalex {n_oa} · crossref {n_cross} · UNRESOLVED {n_unres} · flagged {n_flag}")
    print(f"OUTPUT 1 -> output/{SLUG}-metaanalysis-doi-list.md")

if __name__ == "__main__":
    main()
